#for loop using list
x = ["apple", "cat", "dog", "man"]
for i in x:
    print(i)
#for loop using set
x = {"a", "b", "x", "g"}
for b in x:
    print(b)
#for loop using  dictionary
z = {"gender": "male", "area": "colony"}
for h in z:
    print(h)
#for loop using tuple
t = (9, 8, 7, 6, 5, 4, 3, 2, 1)
for i in t:
    print(i)
#for loop using dictionary  to get both key and values
z = {"gender": "male", "area": "colony"}
for i, h in z.items():
    print(i, h)
